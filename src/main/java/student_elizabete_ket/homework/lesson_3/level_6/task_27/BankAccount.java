package student_elizabete_ket.homework.lesson_3.level_6.task_27;

 class BankAccount {
     private String owner;
     private int money;

     public BankAccount(String owner, int moneyAmount) {
         this.owner = owner;
         this.money = moneyAmount;
     }

     public String getOwner() {
         return owner;
     }

     public int getMoney() {
          return money;
     }

 }
