package teacher.lesson_4.lessoncode;

public class SwitchExample {

    public static void main(String[] args) {

        int number = 6;

        switch (number) {
            case 1:
                System.out.println("User pick = 1");
                break;
            case 2:
                System.out.println("User pick = 2");
                break;
            case 3:
                System.out.println("User pick = 3");
                break;
        }
    }
}
