class Address {

    public String name;
    public String street;
    public int house;
    public int apartment;
    public String city;
    public String country;
    public String zip;

    public Address(String name, String street, int house, int apartment, String city, String country, String zip) {
        this.name = name;
        this.street = street;
        this.house = house;
        this.apartment = apartment;
        this.city = city;
        this.country = country;
        this.zip = zip;
    }
}
