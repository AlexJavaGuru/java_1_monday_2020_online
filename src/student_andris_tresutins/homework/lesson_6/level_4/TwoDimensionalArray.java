public class TwoDimensionalArray {
    public static void main(String[] args) {
        int[][] arr = new int[10][10];
        arr[0][0] = 1;
        arr[1][0] = 2;
        arr[0][1] = 3;


        System.out.println(arr[0][0]);
        System.out.println(arr[1][0]);
        System.out.println(arr[0][1]);
    }
}
